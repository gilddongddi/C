//실습6. 소문자를 대문자로 변경하는 프로그램

#define _CRT_SECURE_NO_WARNINGS
#include<stdio.h>

int main(void)
{
	char letter;
	while (1)
	{
		printf("소문자를 입력하시오: ");
		scanf(" %c", &letter);			//공백문자를 제외시키기 위해 한캄띔

		if (letter == 'Q')
			break;
		if (letter < 'a' || letter>'z')
			continue;

		letter -= 32;
		printf("변환된 대문자는 %c입니다. \n", letter);
	}
	return 0;
}